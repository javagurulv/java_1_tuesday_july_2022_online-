class Calculator {

     int sum(int firstNumber, int secondNumber) {
            return firstNumber + secondNumber;
        }

     int sub(int firstNumber, int secondNumber) {
            return firstNumber - secondNumber;
        }

     int div(int firstNumber, int secondNumber) {
        return firstNumber / secondNumber;
    }

     int mul(int firstNumber, int secondNumber) {
        return firstNumber * secondNumber;
    }

    }


